    public function afterSave($data)
    {
        $templatefile = resource_path('views/email/').$data['slug'].'.blade.php';
        if(file_exists($templatefile)){
            if(is_writable($templatefile)){
                file_put_contents($templatefile, $data['body']);
            }else{
                chmod($templatefile, 0777);
                file_put_contents($templatefile, $data['body']);
            }
        }else{
            touch($templatefile);
            chmod($templatefile, 0777);
            file_put_contents($templatefile, $data['body']);
        }
        return parent::afterSave($data); // TODO: Change the autogenerated stub
    }

